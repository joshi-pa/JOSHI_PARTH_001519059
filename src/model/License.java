public class License {
    
    private String licNum;
    private String licDOI;
    private String licDOE;
    private String licBlTy;
    private String licPic;

    public String getLicNum() {
        return licNum;
    }

    public void setLicNum(String licNum) {
        this.licNum = licNum;
    }

    public String getLicDOI() {
        return licDOI;
    }

    public void setLicDOI(String licDOI) {
        this.licDOI = licDOI;
    }

    public String getLicDOE() {
        return licDOE;
    }

    public void setLicDOE(String licDOE) {
        this.licDOE = licDOE;
    }

    public String getLicBlTy() {
        return licBlTy;
    }

    public void setLicBlTy(String licBlTy) {
        this.licBlTy = licBlTy;
    }

    public String getLicPic() {
        return licPic;
    }

    public void setLicPic(String licPic) {
        this.licPic = licPic;
    }
    
    
    
}
